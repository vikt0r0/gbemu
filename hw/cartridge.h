#ifndef CARTRIDGE_H
#define CARTRIDGE_H

// Size of a cartridge bank
#define CARTRIDGE_BANK_SIZE 0x4000

// $0100-$0103	NOP / JP $0150
#define CARTRIDGE_NOP_JP_150_LOWER 0x0100
#define CARTRIDGE_NOP_JP_150_UPPER 0x0103

// $0104-$0133	Nintendo Logo
#define CARTRIDGE_NINTENDO_LOGO_LOWER 0x0104
#define CARTRIDGE_NINTENDO_LOGO_UPPER 0x0133

// $0134-$013E	Game Title (Uppercase ASCII)
#define CARTRIDGE_GAME_TITLE_LOWER 0x0134
#define CARTRIDGE_GAME_TITLE_UPPER 0x013e

// $013F-$0142	4-byte Game Designation
#define CARTRIDGE_GAME_DESIGNATION_LOWER 0x013f
#define CARTRIDGE_GAME_DESIGNATION_UPPER 0x0142

// $0143	Color Compatibility byte
#define CARTRIDGE_COLOR_COMPATIBILITY_BYTE 0x0143

// $0144-$0145	New Licensee Code
#define CARTRIDGE_NEW_LICENSEE_CODE_LOWER 0x0144
#define CARTRIDGE_NEW_LICENSEE_CODE_UPPER 0x0145

// $0146	SGB Compatibility byte
#define CARTRIDGE_SGB_COMPATIBILITY 0x0146

// $0147	Cart Type
#define CARTRIDGE_CART_TYPE 0x0147

// $0148	Cart ROM size
#define CARTRIDGE_ROM_SIZE 0x0148

// $0149	Cart RAM size
#define CARTRIDGE_RAM_SIZE_LOWER 0x0149

// $014A	Destination code
#define CARTRIDGE_DESTINATION_CODE 0x014a

// $014B	Old Licensee code
#define CARTRIDGE_OLD_LICENSEE_CODE 0x014b

// $014C	Mask ROM version
#define CARTRIDGE_MASK_ROM_VERSION 0x014c

// $014D	Complement checksum
#define CARTRIDGE_COMPLEMENT_CHECKSUM 0x014d

// $014E-$014F	Checksum
#define CARTRIDGE_CHECKSUM_LOWER 0x014e
#define CARTRIDGE_CHECKSUM_UPPER 0x014f

#endif